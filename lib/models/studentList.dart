class StudentList {
  final String id;
  final String name;
  final String regNo;
  final String course;
  final String module;
  final String email;
  final String year;
  final bool isClassRep;

  StudentList({
    required this.id,
    required this.name,
    required this.regNo,
    required this.course,
    required this.module,
    required this.email,
    required this.year,
    required this.isClassRep,
  });
}
